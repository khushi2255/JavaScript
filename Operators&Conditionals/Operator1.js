// Arithmetic Operators
let a = 5;
let b = 4;

console.log("a = ", a, "& b = ", b);
console.log("a + b =", a + b);
console.log("a - b =", a - b);
console.log("a * b =", a * b);
console.log("a / b=", a / b);
console.log("a % b=", a % b);
console.log("a ** b=", a ** b);


// Unary Operator
let x = 10;
let y = 1;

console.log("x=", x, "& y =", y);
