// Logical Operators :-
// Logical AND (&&) : if both the condition is true then it returns true!!. otherwise it return false.
// Logical OR (||) : if atleast one condition is true then it return true!!. otherwise it return false
// Logical NOT (!) : when the condition is true, it return false. when the condition is false , then it return true.
let a = 6;
let b = 3;

let cond1 = a > b;
let cond2 = a === b;
console.log("cond1 && cond2 = ", cond1 && cond2);
console.log("cond1 || cond2 = ", cond1 || cond2);
console.log("!(6 < 3) = ", !(a < b));
